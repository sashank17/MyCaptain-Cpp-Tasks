#include <iostream>
using namespace std;

int main() {
    char a;
    int b;
    float c;
    double d;
    cout << "Size of char: " << sizeof(a) << " byte" << endl;
    cout << "Size of int: " << sizeof(b) << " bytes" << endl;
    cout << "Size of float: " << sizeof(c) << " bytes" << endl;
    cout << "Size of double: " << sizeof(d) << " bytes" << endl;
}
